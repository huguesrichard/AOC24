from collections import OrderedDict

CODE = dict([('.', 0), ('#', -1),
             ('@', -2),('0', 1)])

MOVES = dict([('>', (0,1)), ('^', (-1, 0)), 
              ('<', (0,-1)), ('v', (1,0)) ])


def text2mat(text: str, 
             coding = CODE) -> ndarray:
    """ 
    Function to recode a list of str as a matrix with integer values
    """
    return np.array([[coding[x] for x in s] for s in text.split("\n") if len(s) > 0 ], 
                    dtype = int)


